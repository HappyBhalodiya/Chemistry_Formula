package com.aswdc_chemistryformula.Been;

public class Bean_Molecular {

    private int AtomicID;
    private String ElementSymbol;
    private String AtomicMass;


    public int getAtomicID() {
        return AtomicID;
    }

    public void setAtomicID(int atomicID) {
        AtomicID = atomicID;
    }

    public String getElementSymbol() {
        return ElementSymbol;
    }

    public void setElementSymbol(String elementSymbol) {
        ElementSymbol = elementSymbol;
    }

    public String getAtomicMass() {
        return AtomicMass;
    }

    public void setAtomicMass(String atomicMass) {
        AtomicMass = atomicMass;
    }
}
